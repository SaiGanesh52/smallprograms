class Primitivedatatype
{
    byte b;
    short s;
    int i;
    long l;
    float f;
    double d;
    char ch;
    boolean boo;
    public static void main(String args[])
    {
        Primitivedatatype obj=new Primitivedatatype();
        System.out.println("\tPdatatypes\trange");
        System.out.println("\tbyte= \t\t"  +obj.b);
        System.out.println("\tshort=\t\t"  +obj.s);
        System.out.println("\tbyte=\t\t"   +obj.i);
        System.out.println("\tlong=\t\t"   +obj.l);
        System.out.println("\tfloat=\t\t"  +obj.f);
        System.out.println("\tdouble=\t\t" +obj.d);
        System.out.println("\tcharacter=\t\t"+obj.ch);
        System.out.println("\tboolean=\t"  +obj.boo);

    }
}


