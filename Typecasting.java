//implicit type casting byte->short->int->long->float->double
class Typecasting
{
public static void main(String args[])
{
   int i=333;
   long d=i;
   float f=d;
System.out.println("integer value is"+i);
System.out.println("long value is"+d);
System.out.println("float value is"+f);

}
}